a=int(input())
b=int(input())

sum=a+b
difference= a-b
product=a*b
quotient = a/b
remainder = a%b
result= a**b

print(f"{a} + {b} is {sum}")
print(f"{a} - {b} is {difference}")
print(f"{a} * {b} is {product}")
print(f"{a} / {b} is {quotient}")
print(f"{a} % {b} is {remainder}")
print(f"{a} ^ {b} is {result}")